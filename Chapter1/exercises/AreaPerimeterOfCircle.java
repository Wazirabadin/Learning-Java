package exercises;

public class AreaPerimeterOfCircle {
	public static void main(String[] args) {
		System.out.println("The area of a circle with a radius of 5.5 unit is: ");
		System.out.print("Area = Pi x radius x radius = 3.14 x 5.5 x 5.5 = ");
		System.out.print(3.14 * 5.5 * 5.5 );
		System.out.println(" units.");
		System.out.println();
		
		System.out.println("The perimeter of a circle with a radius of 5.5 unit is: ");
		System.out.print("Perimeter = 2 x Pi x radius = 2 x 3.14 x 5.5 = ");
		System.out.print(2.0 * 3.14 * 5.5);
		System.out.println(" units.");
	}

}
