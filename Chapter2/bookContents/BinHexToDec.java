package bookContents;
// This program demonstrates that binary and hexadecimal can be directly converted to their corresponding decimal value

public class BinHexToDec {
	public static void main(String[] args) {
		// binary to decimal
		System.out.println(0b101);
		System.out.println(0B101);
		
		// hexadecimal to decimal
		System.out.println(0xaf);	
		System.out.println(0XAF);	
	}

}
